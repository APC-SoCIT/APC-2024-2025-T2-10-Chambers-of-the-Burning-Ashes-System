CREATE DATABASE columbary_db;
USE columbary_db;

CREATE TABLE Account (
    account_id INT AUTO_INCREMENT PRIMARY KEY,
    username VARCHAR(45) UNIQUE NOT NULL,
    password VARCHAR(25) NOT NULL
);

CREATE TABLE ParishAdministrator (
    admin_id INT AUTO_INCREMENT PRIMARY KEY,
    account_id INT UNIQUE NOT NULL,
    FOREIGN KEY (account_id) REFERENCES Account(account_id) ON DELETE CASCADE
);

CREATE TABLE ParishStaff (
    staff_id INT AUTO_INCREMENT PRIMARY KEY,
    account_id INT UNIQUE NOT NULL,
    FOREIGN KEY (account_id) REFERENCES Account(account_id) ON DELETE CASCADE
);

CREATE TABLE Customer (
    customer_id INT AUTO_INCREMENT PRIMARY KEY,
    full_name VARCHAR(45) NOT NULL,
    permanent_address TEXT NOT NULL,
    landline_number VARCHAR(15),
    mobile_number VARCHAR(11) NOT NULL,
    email_address VARCHAR(45) DEFAULT 'no-email@example.com',
    status ENUM('pending', 'approved', 'declined') DEFAULT 'pending'
);

CREATE TABLE Beneficiary (
    beneficiary_id INT AUTO_INCREMENT PRIMARY KEY,
    first_beneficiary_name VARCHAR(255) NOT NULL,
    second_beneficiary_name VARCHAR(45),
    third_beneficiary_name VARCHAR(45)
);

CREATE TABLE Payment (
    payment_id INT AUTO_INCREMENT PRIMARY KEY,
    mode_of_payment ENUM('Full Payment', '6-Month Installment') DEFAULT 'Full Payment',
    Full_payment_receipt_1 INT,
    six_month_receipt_1 INT,
    six_month_receipt_2 INT,
    six_month_receipt_3 INT,
    six_month_receipt_4 INT,
    six_month_receipt_5 INT,
    six_month_receipt_6 INT,
    Full_payment_amount_1 DECIMAL(10,2),
    six_month_amount_1 DECIMAL(10,2),
    six_month_amount_2 DECIMAL(10,2),
    six_month_amount_3 DECIMAL(10,2),
    six_month_amount_4 DECIMAL(10,2),
    six_month_amount_5 DECIMAL(10,2),
    six_month_amount_6 DECIMAL(10,2),
    total_amount DECIMAL(10,2)
);

CREATE TABLE HolderOfPrivilege (
    holder_of_privilege_id INT AUTO_INCREMENT PRIMARY KEY,
    full_name VARCHAR(100) NOT NULL,
    email_address VARCHAR(100),
    address VARCHAR(255),
    landline_number VARCHAR(20),
    mobile_number VARCHAR(20)
);

CREATE TABLE ColumbaryRecord (
    vault_id VARCHAR(7) PRIMARY KEY,
    issuance_date DATE,
    expiration_date DATE,
    inurnment_date DATE,
    issuing_parish_priest VARCHAR(45),
    urns_per_columbary ENUM('1', '2', '3', '4'),
    customer_id INT,
    staff_id INT,
    beneficiary_id INT,
    payment_id INT,
    holder_of_privilege_id INT,
    status ENUM('Vacant', 'Occupied') DEFAULT 'Vacant',
    FOREIGN KEY (customer_id) REFERENCES Customer(customer_id) ON DELETE SET NULL,
    FOREIGN KEY (staff_id) REFERENCES ParishStaff(staff_id) ON DELETE SET NULL,
    FOREIGN KEY (beneficiary_id) REFERENCES Beneficiary(beneficiary_id) ON DELETE SET NULL,
    FOREIGN KEY (payment_id) REFERENCES Payment(payment_id) ON DELETE SET NULL,
    FOREIGN KEY (holder_of_privilege_id) REFERENCES HolderOfPrivilege(holder_of_privilege_id) ON DELETE SET NULL
);

CREATE TABLE InquiryRecord (
    letter_of_intent_id INT AUTO_INCREMENT PRIMARY KEY,
    columbary_vault_id VARCHAR(7),
    parish_administrator_id INT,
    parish_staff_id INT,
    customer_id INT,
    FOREIGN KEY (columbary_vault_id) REFERENCES ColumbaryRecord(vault_id) ON DELETE SET NULL,
    FOREIGN KEY (parish_administrator_id) REFERENCES ParishAdministrator(admin_id) ON DELETE SET NULL,
    FOREIGN KEY (parish_staff_id) REFERENCES ParishStaff(staff_id) ON DELETE SET NULL,
    FOREIGN KEY (customer_id) REFERENCES Customer(customer_id) ON DELETE SET NULL
);

CREATE TABLE TwoFactorAuth (
    id INT AUTO_INCREMENT PRIMARY KEY,
    email VARCHAR(45) NOT NULL,
    otp VARCHAR(6) NOT NULL,
    created_at DATETIME DEFAULT CURRENT_TIMESTAMP,
    is_verified BOOLEAN DEFAULT FALSE
);

CREATE TABLE ChatQuery (
    id INT AUTO_INCREMENT PRIMARY KEY,
    user_message TEXT NOT NULL,
    bot_response TEXT NOT NULL,
    created_at DATETIME DEFAULT CURRENT_TIMESTAMP
);

